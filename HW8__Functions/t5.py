# Користувач робить внесок в розмірі n гривень строком на years років
# під 10% річних (щороку розмір його внеску збільшується на 10%.
# Ці гроші додаються до суми вкладу, і на них в наступному році
# теж будуть відсотки).
# Написати функцію bank, яка приймає аргументи n і years, і повертає суму,
# яка буде на рахунку користувача.


def bank(n, years):
    p = 10
    result = n
    for i in range(years):
        result += (result/100) * p
    return result


print(bank(100, 10))
